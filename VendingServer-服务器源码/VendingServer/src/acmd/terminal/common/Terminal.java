package acmd.terminal.common;

public class Terminal {
	int terminalId;
	String terminalNumber;
	String terminalPosition;
	String terminalUUID;
	String termianlSerial;
	public int getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(int terminalId) {
		this.terminalId = terminalId;
	}
	public String getTerminalNumber() {
		return terminalNumber;
	}
	public void setTerminalNumber(String terminalNumber) {
		this.terminalNumber = terminalNumber;
	}
	public String getTerminalPosition() {
		return terminalPosition;
	}
	public void setTerminalPosition(String terminalPosition) {
		this.terminalPosition = terminalPosition;
	}
	public String getTerminalUUID() {
		return terminalUUID;
	}
	public void setTerminalUUID(String terminalUUID) {
		this.terminalUUID = terminalUUID;
	}
	public String getTermianlSerial() {
		return termianlSerial;
	}
	public void setTermianlSerial(String termianlSerial) {
		this.termianlSerial = termianlSerial;
	}
}
